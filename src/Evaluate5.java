public class Evaluate5 {
    public static void main(String[] args){
        int a=10,b=20;
        System.out.println("++a-b-- = "+(++a-b--));
        System.out.println("a%b++ = "+(a%b++));
        System.out.println("a*=b+5 = "+(a*=b+5));
        int x=69>>2;
        System.out.println("x = "+x);

    }
}
